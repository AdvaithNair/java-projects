    private boolean isInt(javax.swing.JTextField text){
        char[] chars  = text.getText().toCharArray(); //converts text to chars
        for (int i = 0; i < chars.length; i++){
            if (!Character.isDigit(chars[i])){ //tests each character
                return false;
            }
        }
        return true;
    }

