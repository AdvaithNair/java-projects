    private void testButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_testButtonActionPerformed
        // TODO add your handling code here:
        if(!isInt(monthField) || !isInt(dayField) || !isInt(day2Field) || !isInt(month2Field) || !isInt(yearField)){
            JOptionPane.showMessageDialog(null, "Please enter numeric values in the text fields!");
        }
        else{
            int month = Integer.parseInt(monthField.getText());
            int day = Integer.parseInt(dayField.getText());
            int month2 = Integer.parseInt(month2Field.getText());
            int day2 = Integer.parseInt(day2Field.getText());
            int year = Integer.parseInt(yearField.getText()); //get all the values
            
            Date date1 = new Date(day, month, year);
            Date date2 = new Date(day2, month2, year); //create objects
            
            String leapOut = "";
            stringDateField.setText(date1.toString());
            if(Date.isLeap(year)){
                leapOut = "Yes";
            }
            else{
                leapOut = "No";
            } //determine leap outputs
            
            leapYearField.setText(leapOut);
            daysBetweenField.setText("" + date1.daysInBetween(date2));
            dayNumberField.setText("" + date1.daysInYear()); //sets values
        }
    }//GEN-LAST:event_testButtonActionPerformed

